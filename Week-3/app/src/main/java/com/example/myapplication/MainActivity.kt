@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    var showDialog by remember { mutableStateOf(false) }

    Button (onClick = {showDialog = true}, modifier = modifier) {Text("Click Here")}

    if (showDialog) {
        AlertDialog(
            onDismissRequest = { showDialog = false },
            title = { Text("Dialog Shown Here") },
            confirmButton = {
                Button(onClick = { showDialog = false }) {
                    Text("OK")
                }
            },
            dismissButton = {
                Button(onClick = { showDialog = false }) {
                    Text("Cancel")
                }
            }
        )
    }
}

@Composable
fun InputField() {
    var text by remember { mutableStateOf("") }

    TextField(value = text, onValueChange = {text = it}, label = { Text("Enter your name") }, placeholder = { Text("Enter your name") }

    )
}

